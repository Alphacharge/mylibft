#include "libft.h"

/*identify any kind of space*/
int	ft_isspace(int c)
{
	if (c == ' ' || c == '\t' || c == '\r' )
		return (1);
	if (c == '\n' || c == '\v' || c == '\f')
		return (1);
	return (0);
}
